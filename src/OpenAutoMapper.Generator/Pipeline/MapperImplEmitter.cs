    private static void EmitMapTwoGenericUpdate(SourceStringBuilder sb, List<TypePairDescriptor> typePairs)
    {
        sb.AppendLine("public TDestination Map<TSource, TDestination>(TSource source, TDestination destination)");
        sb.OpenBrace();

        sb.AppendLine("if (source is null)");
        sb.Indent();
        sb.AppendLine("throw new global::System.ArgumentNullException(nameof(source));");
        sb.Unindent();
        sb.AppendLine("if (destination is null)");
        sb.Indent();
        sb.AppendLine("throw new global::System.ArgumentNullException(nameof(destination));");
        sb.Unindent();
        sb.AppendLine();

        foreach (var pair in typePairs)
        {
            if (pair.MappingName is not null)
                continue;
            sb.AppendLine($"if (source is {pair.SourceFullName} src_{SanitizeName(pair.SourceName)} && destination is {pair.DestFullName} dest_{SanitizeName(pair.DestName)})");
            sb.OpenBrace();

            var srcVar = $"src_{SanitizeName(pair.SourceName)}";
            var dstVar = $"dest_{SanitizeName(pair.DestName)}";

            if (pair.BeforeMapExpression is not null)
            {
                sb.AppendLine($"((global::System.Action<{pair.SourceFullName}, {pair.DestFullName}>)({pair.BeforeMapExpression}))({srcVar}, {dstVar});");
            }

            EmitUpdatePropertyAssignments(sb, pair, srcVar, dstVar);

            if (pair.AfterMapExpression is not null)
            {
                sb.AppendLine($"((global::System.Action<{pair.SourceFullName}, {pair.DestFullName}>)({pair.AfterMapExpression}))({srcVar}, {dstVar});");
            }

            sb.AppendLine($"return (TDestination)(object){dstVar};");
            sb.CloseBrace();
        }

        sb.AppendLine();
        sb.AppendLine("throw new global::System.InvalidOperationException(");
        sb.Indent();
        sb.AppendLine("$\"No mapping configured from {typeof(TSource).FullName} to {typeof(TDestination).FullName}.\");");
        sb.Unindent();

        sb.CloseBrace();
    }

