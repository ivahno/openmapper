    /// <summary>
    /// Generates the OpenAutoMapperImpl class that implements IMapper with
    /// a type-switch dispatch for all discovered mappings.
    /// </summary>
    public static string EmitMapperImplementation(List<TypePairDescriptor> allTypePairs)
    {
        // Sort type pairs so derived types appear before their base types in if-chains.
        var sorted = SortDerivedBeforeBase(allTypePairs);

        var sb = new SourceStringBuilder();

        var needsCollections = sorted.Any(tp => tp.PropertyMatches.Any(m => m.ConversionKind == ConversionKind.Collection));
        var needsDictionary = sorted.Any(tp => tp.PropertyMatches.Any(m => m.ConversionKind == ConversionKind.Dictionary));

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        if (needsCollections || needsDictionary)
        {
            sb.AppendLine("using System.Collections.Generic;");
        }
        if (needsCollections)
        {
            sb.AppendLine("using System.Linq;");
        }

        // Add using directives for namespaces where extension methods are generated
        var extensionNamespaces = new HashSet<string>(StringComparer.Ordinal);
        foreach (var pair in sorted)
        {
            var ns = GetNamespace(pair.DestFullName);
            if (!string.IsNullOrEmpty(ns) && !string.Equals(ns, "OpenAutoMapper.Generated", StringComparison.Ordinal))
            {
                extensionNamespaces.Add(ns);
            }
        }

        foreach (var ns in extensionNamespaces)
        {
            sb.AppendLine($"using {ns};");
        }

        sb.AppendLine();
        sb.AppendLine("namespace OpenAutoMapper.Generated;");
        sb.AppendLine();
        sb.AppendLine("[global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"OpenAutoMapper.Generator\", \"1.0.0.0\")]");
        sb.AppendLine("internal sealed class OpenAutoMapperImpl : global::OpenAutoMapper.IMapper");
        sb.OpenBrace();

        // Map<TDest>(object source) -- single generic parameter
        EmitMapSingleGeneric(sb, sorted);
        sb.AppendLine();

        // Map<TSource, TDest>(TSource source) -- two generic parameters, new instance
        EmitMapTwoGeneric(sb, sorted);
        sb.AppendLine();

        // Map<TSource, TDest>(TSource source, TDest destination) -- update existing
        EmitMapTwoGenericUpdate(sb, sorted);
        sb.AppendLine();

        // Map(object source, Type sourceType, Type destinationType) -- non-generic
        EmitMapNonGeneric(sb, sorted);
        sb.AppendLine();

        // Map(object source, object destination, Type sourceType, Type destinationType) -- non-generic update
        EmitMapNonGenericUpdate(sb, sorted);
        sb.AppendLine();

        // Map<TSource, TDest>(TSource source, string mappingName) -- named mapping dispatch
        EmitMapNamedGeneric(sb, sorted);

        sb.CloseBrace();

        return sb.ToString();
    }

    /// <summary>
    /// Generates the factory initializer that hooks into MapperConfiguration.MapperFactory.
    /// </summary>
    public static string EmitFactoryInitializer()
    {
        var sb = new SourceStringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Runtime.CompilerServices;");
        sb.AppendLine();
        sb.AppendLine("namespace OpenAutoMapper.Generated;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Module initializer that registers the source-generated mapper factory.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("[global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"OpenAutoMapper.Generator\", \"1.0.0.0\")]");
        sb.AppendLine("internal static class OpenAutoMapperFactoryInit");
        sb.OpenBrace();

        sb.AppendLine("[ModuleInitializer]");
        sb.AppendLine("internal static void Initialize()");
        sb.OpenBrace();
        sb.AppendLine("// Register the generated mapper factory so that MapperConfiguration.CreateMapper() works.");
        sb.AppendLine("// Uses reflection to set the internal static property since the generator cannot reference Core directly.");
        sb.AppendLine("var configType = typeof(global::OpenAutoMapper.MapperConfiguration);");
        sb.AppendLine("var factoryProperty = configType.GetProperty(\"MapperFactory\",");
        sb.Indent();
        sb.AppendLine("System.Reflection.BindingFlags.Static | System.Reflection.BindingFlags.NonPublic);");
        sb.Unindent();
        sb.AppendLine("if (factoryProperty is not null)");
        sb.OpenBrace();
        sb.AppendLine("factoryProperty.SetValue(null, new global::System.Func<global::OpenAutoMapper.MapperConfiguration, global::OpenAutoMapper.IMapper>(");
        sb.Indent();
        sb.AppendLine("config => new OpenAutoMapperImpl()));");
        sb.Unindent();
        sb.CloseBrace();

        sb.CloseBrace();

        sb.CloseBrace();

        return sb.ToString();
    }

    /// <summary>
    /// Sort type pairs so derived types come before their base types in dispatch chains.
    /// This ensures "if (source is Dog ...)" is checked before "if (source is Animal ...)".
    /// </summary>
    private static List<TypePairDescriptor> SortDerivedBeforeBase(List<TypePairDescriptor> typePairs)
    {
        // Collect all derived->base relationships
        var derivedSources = new HashSet<string>(StringComparer.Ordinal);
        foreach (var tp in typePairs)
        {
            foreach (var derived in tp.IncludedDerivedTypes)
            {
                derivedSources.Add(derived.SourceFullName);
            }
        }

        // Derived types first, then base types, then others (preserve relative order)
        var derivedPairs = new List<TypePairDescriptor>();
        var basePairs = new List<TypePairDescriptor>();
        var otherPairs = new List<TypePairDescriptor>();

        foreach (var tp in typePairs)
        {
            if (derivedSources.Contains(tp.SourceFullName))
                derivedPairs.Add(tp);
            else if (tp.IsPolymorphicBase)
                basePairs.Add(tp);
            else
                otherPairs.Add(tp);
        }

        var result = new List<TypePairDescriptor>(typePairs.Count);
        result.AddRange(derivedPairs);
        result.AddRange(otherPairs);
        result.AddRange(basePairs);
        return result;
    }

    private static void EmitMapSingleGeneric(SourceStringBuilder sb, List<TypePairDescriptor> typePairs)
    {
        sb.AppendLine("public TDestination Map<TDestination>(object source)");
        sb.OpenBrace();

        sb.AppendLine("if (source is null)");
        sb.Indent();
        sb.AppendLine("throw new global::System.ArgumentNullException(nameof(source));");
        sb.Unindent();
        sb.AppendLine();

        foreach (var pair in typePairs)
        {
            // Skip named mappings in default dispatch
            if (pair.MappingName is not null)
                continue;

            var methodName = MappingCodeEmitter.GetMapMethodName(pair);
            sb.AppendLine($"if (source is {pair.SourceFullName} src_{SanitizeName(pair.SourceName)} && typeof(TDestination) == typeof({pair.DestFullName}))");
            sb.Indent();
            sb.AppendLine($"return (TDestination)(object)src_{SanitizeName(pair.SourceName)}.{methodName}();");
            sb.Unindent();
        }

        sb.AppendLine();
        sb.AppendLine("throw new global::System.InvalidOperationException(");
        sb.Indent();
        sb.AppendLine("$\"No mapping configured from {source.GetType().FullName} to {typeof(TDestination).FullName}.\");");
        sb.Unindent();

        sb.CloseBrace();
    }

